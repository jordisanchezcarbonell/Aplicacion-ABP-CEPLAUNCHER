package com.example.ceplauncher.activities;

public class Actividades {
    private String nom;
    private int imgaen;
    public Actividades(String nom, int imgaen) {
        this.nom = nom;
        this.imgaen = imgaen;
    }
    public String getNom() {
        return nom;
    }
    public int getImgaen() {
        return imgaen;
    }
}

